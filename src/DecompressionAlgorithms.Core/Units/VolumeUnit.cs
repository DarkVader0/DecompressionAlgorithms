namespace DecompressionAlgorithms.Core.Units;

/// <summary>
/// This class represents the different volume units available in the decompression algorithm. The volume units are
/// - Liter: A unit of volume, commonly used in metric systems.
/// - CubicFeet: A unit of volume, commonly used in imperial systems.
/// </summary>
public enum VolumeUnit : byte
{
    Liter,
    CubicFeet
}