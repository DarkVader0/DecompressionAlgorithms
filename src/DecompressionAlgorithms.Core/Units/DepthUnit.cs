namespace DecompressionAlgorithms.Core.Units;

/// <summary>
/// This class represents the different depth units available in the decompression algorithm. The depth units are:
/// - Meter: A unit of measurement for depth, commonly used in metric systems.
/// - Feet: A unit of measurement for depth, commonly used in imperial systems.
/// </summary>
public enum DepthUnit : byte
{
    Meter,
    Feet
}